package org.zsazu;

public class SimpleCalculator {
    SimpleCalculator(){}
    public int add(int a, int b){
        return a+b;
    }
    public int subtract(int a, int b){
        return a-b;
    }
    public int multiply(int a, int b){
        return a*b;
    }
    public double divide(int a, int b){
        return a/b;
    }
    public int modulo(int a, int b){
        return a%b;
    }
}
